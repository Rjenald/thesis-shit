  static List<KaraokeSong> searchSongs(String query) {
    if (query.isEmpty) return songs;
    final q = query.toLowerCase();
    return songs
        .where((s) =>
            s.title.toLowerCase().contains(q) ||
            s.artist.toLowerCase().contains(q))
        .toList();
  }

  static List<KaraokeSong> getSongsByLanguage(String language) =>
      songs.where((s) => s.language == language).toList();

  static List<KaraokeSong> getOfflineSongs() =>
      songs.where((s) => s.offline).toList();

  static List<KaraokeSong> getRandomSongs({int count = 10}) {
    final shuffled = List<KaraokeSong>.from(songs)..shuffle();
    return shuffled.take(count).toList();
  }

  static List<String> getAllArtists() {
    final artists = <String>{};
    for (final s in songs) {
      artists.add(s.artist);
    }
    return artists.toList()..sort();
  }

  static int getTotalSongs() => songs.length;

  static List<KaraokeSong> getSongsByArtist(String artist) =>
      songs.where((s) => s.artist == artist).toList();
