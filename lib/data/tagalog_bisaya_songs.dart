  /// Search songs by title or artist
  static List<KaraokeSong> searchSongs(String query) {
    if (query.isEmpty) return songs;

    final lowerQuery = query.toLowerCase();
    return songs
        .where((song) =>
            song.title.toLowerCase().contains(lowerQuery) ||
            song.artist.toLowerCase().contains(lowerQuery))
        .toList();
  }

  /// Get songs by language
  static List<KaraokeSong> getSongsByLanguage(String language) {
    return songs.where((song) => song.language == language).toList();
  }

  /// Get random songs
  static List<KaraokeSong> getRandomSongs({int count = 10}) {
    final random = <KaraokeSong>[];
    final shuffled = List<KaraokeSong>.from(songs)..shuffle();
    return shuffled.take(count).toList();
  }

  /// Get all artists
  static List<String> getAllArtists() {
    final artists = <String>{};
    for (var song in songs) {
      artists.add(song.artist);
    }
    return artists.toList()..sort();
  }

  /// Get total song count
  static int getTotalSongs() => songs.length;

  /// Get songs by artist
  static List<KaraokeSong> getSongsByArtist(String artist) {
    return songs.where((song) => song.artist == artist).toList();
  }
