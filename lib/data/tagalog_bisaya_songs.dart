/// Karaoke Song Model
class KaraokeSong {
  final String title;
  final String artist;
  final String language; // 'Tagalog' or 'Bisaya'

  const KaraokeSong({
    required this.title,
    required this.artist,
    required this.language,
  });

  /// Get display name
  String get displayName => '$title - $artist';

  /// Get YouTube search query
  String get youtubeQuery => '$title $artist karaoke';

  @override
  String toString() => displayName;

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is KaraokeSong &&
          runtimeType == other.runtimeType &&
          title == other.title &&
          artist == other.artist;

  @override
  int get hashCode => title.hashCode ^ artist.hashCode;
}
