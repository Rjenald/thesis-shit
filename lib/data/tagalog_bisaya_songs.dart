class KaraokeSong {
  final String title;
  final String artist;
  final String language;
  final bool offline;

  const KaraokeSong({
    required this.title,
    required this.artist,
    required this.language,
    this.offline = false,
  });

  String get displayName => '$title - $artist';

  @override
  String toString() => displayName;

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is KaraokeSong &&
          runtimeType == other.runtimeType &&
          title == other.title &&
          artist == other.artist;

  @override
  int get hashCode => title.hashCode ^ artist.hashCode;
}
